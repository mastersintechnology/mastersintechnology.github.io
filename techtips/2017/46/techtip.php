<h2>Automatic Table of Contents in Microsoft Word</h2>
<p>
	Books have tables of contents because it is a good form of organization and allows readers to quickly find what they are looking for in the book. Word documents can also have tables of contents that automatically update to indicate what pages your chapters/sections start on.<br><br>
	In the 'References' tab, you can insert a Table of Contents. If there are no headings (explained later), the table of contents will be empty.<br>
	<img src="/techtips/2017/36/contents1.jpg"><br>
	<img src="/techtips/2017/36/contents2.jpg"><br>
	<img src="/techtips/2017/36/contents3.jpg" width="800" height="221"><br><br>
	To add headings, go back to the 'Home' tab and make your text have a 'Heading' style. If your window width is too low to show all of them, this may be compressed into a single 'Styles' button.<br>
	<img src="/techtips/2017/36/contents4.5.jpg" height="110">&nbsp;&nbsp;&nbsp;<img src="/techtips/2017/36/contents4.jpg" width="370" height="110"><br><br>
	Click inside the table of contents to edit it. The table will be highlighted as shown in the image below, which also shows a heading with the 'Heading 1' style.<br>
	<img src="/techtips/2017/36/contents5.jpg"><br>
	Click the triangle in the blue bar and click 'Update table' to automatically load all new headings and update the page numbers.<br>
