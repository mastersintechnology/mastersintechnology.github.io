<h2>Changing the Line Spacing in Microsoft Word</h2>
<p>
	You can change how much space is left between each line in Word. Select the text whose spacing you want to change and click the line spacing button and select the desired line spacing. Click 'Line Spacing Options...' for more advanced options such as paragraph spacing.<br>
